 #operator prebandingan
#1. sama dengan ==
#2. tidak sama dengan !=
#3. lebih besar dari >
#4. lebih kecil dari <
#5. lebih besar atau sama dengan >=
#6. lebih kecil atau sama dengan <=
 #sama dengan
print(5==8)

#tidak sama dengan 
print(5!=8)

#lebih besar dari
print(5>8)

#lebih kecil dari 
print(5<8)

#lebih besar atau sama dengan 
print(5>=8)

#lebih kecil atau sama dengan 
print(5<=8)